        if($id == 'all'){
            foreach ($users as $user) {
                if ($user['role'] != 1){
                    $user->delete();
                }
            }
        }else{
            $user = User::find($id);
            $user->delete();

        }
        foreach($user->orders as $key=> $order){
            Order::find($order['id'])->delete();

        }
        //dd();
        return redirect()->back();
    }

